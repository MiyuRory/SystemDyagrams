const svg = d3.select("svg");
const width = +svg.attr("width");
const height = +svg.attr("height");

// Creacin de nodos y enlaces
const nodes = [];
const links = [];

data.Entidades.forEach(e => {
    nodes.push({ id: e.Entidad });
    e.Influye.forEach(rel => {
        links.push({
            source: e.Entidad,
            target: rel.Entidad,
            polarity: rel.Polaridad
        });
    });
});

const simulation = d3.forceSimulation(nodes)
    .force("link", d3.forceLink(links).id(d => d.id).distance(500))
    .force("charge", d3.forceManyBody().strength(-400))
    .force("center", d3.forceCenter(svg.attr("width") / 2, svg.attr("height") / 2))
.force("collide", d3.forceCollide().radius(function(d) {
        return 100; // The radius for collision detection. Adjust as necessary.
    }));


// Define arrowhead markers for the links
svg.append("defs").selectAll("marker")
    .data(["Positiva", "Negativa"]) // Different marker IDs
    .enter().append("marker")
    .attr("id", d => d)
    .attr("viewBox", "0 -5 10 10")
    .attr("refX", 25) // This sets how far back the arrow is set. Adjust as needed.
    .attr("refY", 0)
    .attr("markerWidth", 6)
    .attr("markerHeight", 6)
    .attr("orient", "auto")
    .append("path")
    .attr("d", "M0,-5L10,0L0,5")
	.attr("refX", 35) 
	.attr("markerWidth", 12)
	.attr("markerHeight", 12)
    .style("fill", d => (d === "Positiva" ? "green" : "red"));

	
// Draw the links with arrowheads
const link = svg.append("g")
    .selectAll("line")
    .data(links)
    .enter().append("line")
    .attr("Polaridad", d => d.polarity) 
    .attr("stroke-width", 2) // Set a stroke width
    .attr("marker-end", d => `url(#${d.polarity})`);



	
// After the existing 'link' definition
const linkLabel = svg.append("g")
    .selectAll("text")
    .data(links)
    .enter().append("text")
    .attr("font-family", "Arial, sans-serif")
    .attr("font-size", "12px")
    .attr("fill", "#000")
    .text(d => d.polarity === "Positiva" ? "+" : "-");


// Definir marcadores de flechas
svg.append("defs").selectAll("marker")
    .data(["arrow"])
    .enter().append("marker")
    .attr("id", d => d)
    .attr("viewBox", "0 -5 10 10")
    .attr("refX", 25)
    .attr("refY", 0)
    .attr("markerWidth", 6)
    .attr("markerHeight", 6)
    .attr("orient", "auto")
    .append("path")
    .attr("d", "M0,-5L10,0L0,5")
    .attr("fill", "black");

// Dibujar nodos
const node = svg.append("g")
    .selectAll("circle")
    .data(nodes)
    .enter().append("circle")
    .attr("r", 50) // Aumentado para que quepa el texto
    .call(d3.drag()
        .on("start", dragstarted)
        .on("drag", dragged)
        .on("end", dragended));

// Append text for the nodes
const nodeText = svg.append("g")
    .selectAll("text")
    .data(nodes)
    .enter().append("text")
    .text(d => d.id)
    .attr("font-family", "Arial, sans-serif")
    .attr("font-size", "12px")
    .attr("fill", "#333")
    .attr("text-anchor", "middle")
    .attr("dy", ".35em"); // Vertical alignment

// Agregar texto a enlaces
link.append("text")
    .text(d => d.polarity)
    .attr("dx", 15)
    .attr("dy", -5);

simulation.on("tick", () => {
    link.attr("x1", d => d.source.x)
        .attr("y1", d => d.source.y)
        .attr("x2", d => d.target.x)
        .attr("y2", d => d.target.y);

    node.attr("cx", d => d.x)
        .attr("cy", d => d.y);
		
	// Add this block to update the position of the node texts
    nodeText.attr("x", d => d.x)
        .attr("y", d => d.y);
		
	// Add this block to position the link labels at the midpoint of each link
    linkLabel.attr("x", d => (d.source.x + d.target.x) / 2)
        .attr("y", d => (d.source.y + d.target.y) / 2);
});

function dragstarted(d) {
    if (!d3.event.active) simulation.alphaTarget(0.3).restart();
    d.fx = d.x;
    d.fy = d.y;
}

function dragged(d) {
    d.fx = d3.event.x;
    d.fy = d3.event.y;
}

function dragended(d) {
    if (!d3.event.active) simulation.alphaTarget(0);
    d.fx = null;
    d.fy = null;
}